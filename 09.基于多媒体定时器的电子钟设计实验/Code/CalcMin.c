/*********************************************************************************************************
* 函数名称: CalcMin
* 函数功能: 计算分钟
* 输入参数: tick，即以秒为单位的计数值 
* 输出参数: void
* 返 回 值: 分钟值
* 创建日期: 2021年03月01日
* 注    意: 
*********************************************************************************************************/
short CalcMin(int tick)
{
  short min;
  
  min = (tick % 3600) / 60;    //tick对3600取余后再对60取模赋值给min 
  
  return(min);
}
