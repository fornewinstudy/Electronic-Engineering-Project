/*********************************************************************************************************
* 函数名称: CalcHour
* 函数功能: 计算小时
* 输入参数: tick，即以秒为单位的计数值 
* 输出参数: void
* 返 回 值: 小时值
* 创建日期: 2018年03月01日
* 注    意: 
*********************************************************************************************************/
short CalcHour(int tick)
{
  short hour;

  hour = tick / 3600;          //tick对3600取模赋值给hour
  
  return(hour);
}
