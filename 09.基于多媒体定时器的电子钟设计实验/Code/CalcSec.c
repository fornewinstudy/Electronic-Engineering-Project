/*********************************************************************************************************
* 函数名称: CalcSec
* 函数功能: 计算秒
* 输入参数: tick，即以秒为单位的计数值 
* 输出参数: void
* 返 回 值: 秒值
* 创建日期: 2021年03月01日
* 注    意: 
*********************************************************************************************************/
short CalcSec(int tick)
{
  short sec;
  
  sec = (tick % 3600) % 60;    //tick对3600取余后再对60取余赋值给sec
  
  return(sec);
}